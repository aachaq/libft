#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*j;
	int		i;

	j = 0;
	i = 0;
	while (*s != '\0')
	{
		if (*s == (char) c)
			j = (char *)s;
		s++;
	}
	if (c == '\0')
		j = (char *)s;
	return (j);
}
